def get_lang_shortcut(lang: int) -> str:
    match lang:
        case 1:
            return "python"
        case 2:
            return "js"
        case 3:
            return "java"
        case 4:
            return "html"
        case 5:
            return "css"
        case 6:
            return "cpp"
        case 6:
            return 'c'
        case 7:
            return "ts"